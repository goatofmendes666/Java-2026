public class ArithmaticOp
{
    public static void main(String[] args)
    {
        int a = 20;
        int b = 4;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);

        int z = 5;
        ++z;
        System.out.println(z);
        --z;
        System.out.println(z);
    }
}
